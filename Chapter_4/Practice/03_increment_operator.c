#include<stdio.h>
 
int main(){
 
    int i = 5;
    
    printf("the value of ++i is %d\n", ++i);
    //++i means it increment first and then it will print.

    printf("the value of i++ is %d\n", i++);
    //i++ means it print first and then it increment.

    printf("the value of i is %d\n", i);
    //i print like normal as like always.

     

return 0;
}